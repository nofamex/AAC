import WordSearch from "@blex41/word-search";

export default function generateWord() {
  const options = {
    cols: 22,
    rows: 22,
    dictionary: simulWordArr,
    maxWords: 42,
    upperCase: true,
    diacritics: true,
  };
  const ws = new WordSearch(options);
  let grids = ws.grid;
  const words = ws.words;
  grids = simulQuestion;
  return {
    instance: ws,
    grids,
    words,
  };
}

